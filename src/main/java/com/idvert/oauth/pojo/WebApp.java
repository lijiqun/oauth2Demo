package com.idvert.oauth.pojo;

public class WebApp {
	
	private Long webId;
	
	private String webKey;
	
	private String webName;
	
	private String webProfile;
	
	private String webCategory;
	
	private String webUrl;
	
	private String webState;

	public Long getWebId() {
		return webId;
	}

	public void setWebId(Long webId) {
		this.webId = webId;
	}

	public String getWebKey() {
		return webKey;
	}

	public void setWebKey(String webKey) {
		this.webKey = webKey;
	}

	public String getWebName() {
		return webName;
	}

	public void setWebName(String webName) {
		this.webName = webName;
	}

	public String getWebProfile() {
		return webProfile;
	}

	public void setWebProfile(String webProfile) {
		this.webProfile = webProfile;
	}

	public String getWebCategory() {
		return webCategory;
	}

	public void setWebCategory(String webCategory) {
		this.webCategory = webCategory;
	}

	public String getWebUrl() {
		return webUrl;
	}

	public void setWebUrl(String webUrl) {
		this.webUrl = webUrl;
	}

	public String getWebState() {
		return webState;
	}

	public void setWebState(String webState) {
		this.webState = webState;
	}
	
	
	
}
