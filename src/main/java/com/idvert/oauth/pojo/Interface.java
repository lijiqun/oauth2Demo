package com.idvert.oauth.pojo;

public class Interface {
	
	/**接口Id*/
	private Long interfaceId;
	/**接口名称*/
	private String interfaceName;
	/**接口地址*/
	private String interfaceUrl;
	/**接口简介*/
	private String interfaceProfile;
	/**接口状态*/
	private int interfaceState;
	
	public Long getInterfaceId() {
		return interfaceId;
	}
	public void setInterfaceId(Long interfaceId) {
		this.interfaceId = interfaceId;
	}
	public String getInterfaceName() {
		return interfaceName;
	}
	public void setInterfaceName(String interfaceName) {
		this.interfaceName = interfaceName;
	}
	public String getInterfaceUrl() {
		return interfaceUrl;
	}
	public void setInterfaceUrl(String interfaceUrl) {
		this.interfaceUrl = interfaceUrl;
	}
	public String getInterfaceProfile() {
		return interfaceProfile;
	}
	public void setInterfaceProfile(String interfaceProfile) {
		this.interfaceProfile = interfaceProfile;
	}
	public int getInterfaceState() {
		return interfaceState;
	}
	public void setInterfaceState(int interfaceState) {
		this.interfaceState = interfaceState;
	}
	
	

}
